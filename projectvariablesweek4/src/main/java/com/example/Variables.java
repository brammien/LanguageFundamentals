package com.example;

public class Variables
{
    public static void main(String[] args) {
        int num1 = 4;
        boolean b = true;
        byte bb = 55;
        short s = 1001;
        long l = 10L;
        double d = 55.23;
        char c = 'a';
        float f = 65f;

        System.out.println(num1);
        //output for value int
        System.out.println(bb);
        //output for value byte
        System.out.println(s);
        //output for value short
        System.out.println(l);
        //output for value long
        System.out.println(d);
        //output for value double
        System.out.println(c);
        //output for value char
        System.out.println(f);
        //output for value float

        if (l > s);
            System.out.println("hello world");
            //output value for boolean

    }

}
